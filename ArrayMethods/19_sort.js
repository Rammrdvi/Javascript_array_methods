/*  Sorts the elements of an array in place and returns the sorted array. */

let arr= [4,1,3,2,5];

arr.sort();

console.log(arr); // output : [ 1, 2, 3, 4, 5 ]


let array = ["Mango","Kiwi", "Banana", "Apple","Orange"];

array.sort();
console.log(array); // output : [ 'Apple', 'Banana', 'Kiwi', 'Mango', 'Orange' ]

// It will return the array in ascending order 